module Demo where

----------------------------------------------------------------------------------------------------------------
concatList :: [[a]] -> [a]
concatList = foldr (++) []
----------------------------------------------------------------------------------------------------------------
lengthList :: [a] -> Int
lengthList = foldr (\x s -> s + 1) 0
----------------------------------------------------------------------------------------------------------------
sumOdd :: [Integer] -> Integer
sumOdd = foldr (\x s -> if odd x then s + x else s) 0
